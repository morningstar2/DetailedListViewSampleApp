import UIKit

extension UIColor {
  static var fushia : UIColor {
    get {
      return UIColor(red: 255/255.0, green: 0/255.0, blue: 128/255.0, alpha: 1.0)
    }
  }
  
  static var navyBlue : UIColor {
    get {
      return UIColor(red: 0/255.0, green: 0/255.0, blue: 128/255.0, alpha: 1.0)
    }
  }
  
  static var darkYellow : UIColor {
    get {
      return UIColor(red: 152/255.0, green: 148/255.0, blue: 97/255.0, alpha: 1.0)
    }
  }
}
